Trattamento dei dati personali: consenso obbligatorio e consenso facoltativo  
Il trattamento dei dati personali è di regola ammesso solo con il consenso degli interessati, che 
deve essere libero, informato, espresso in forma specifica e documentato per iscritto. L’interessato 
è libero di fornire i dati personali di volta in volta richiesti; il loro mancato, parziale o inesatto 
conferimento potrebbe comportare l’impossibilità di fornire i servizi richiesti (art 7 GDPR).  
L’acquisizione del consenso al trattamento dei dati personali è necessaria per tutti i trattamenti 
sopra indicati connessi e/o necessari ad adempiere ad obblighi di legge, alla normativa 
comunitaria, per eseguire obblighi derivanti da un contratto di cui è parte l’interessato o per 
adempiere, prima della conclusione del contratto, a specifiche richieste di quest’ultimo (finalità: 
punti 1-2-3-4-7).  
Il consenso al trattamento dei dati personali per le finalità di cui ai punti 5-6 è invece facoltativo e 
potrà essere revocato in ogni momento secondo le modalità descritte nella sezione “Diritti 
dell’interessato” della presente informativa. L’eventuale rifiuto dell’interessato a rispondere per i 
punti 5 e 6 comporterà la nostra impossibilità di inviare materiale pubblicitario e svolgere attività 
promozionali.  
