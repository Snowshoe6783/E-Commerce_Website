Modalità del trattamento e conservazione  
I dati personali sono trattati con strumenti cartacei, informatici, telematici, automatizzati 
comprendente profilazione (art 22 GDPR); La profilazione può avvenire utilizzando dati individuali o 
identificativi (es. dati anagrafici), oppure dati aggregati derivanti da dati personali individuali. Il 
tutto nel rispetto delle garanzie di riservatezza e delle misure di sicurezza previste dalla normativa 
vigente, con logiche strettamente correlate alle finalità̀ del trattamento.  
Specifiche misure di sicurezza sono osservate per prevenire la perdita dei dati, usi illeciti o non 
corretti ed accessi non autorizzati.  
Il periodo di conservazione dei dati è legato alla finalità del trattamento in corso. Legea non 
tratterrà le informazioni personali più di quanto non sia necessario per adempiere alle finalità per 
cui tali informazioni sono state elaborate, inclusa la sicurezza della nostra procedura di 
elaborazione in conformità con gli obblighi normativi e legali (ad es. controllo, contabilità e termini 
di conservazione legale), la gestione delle controversie e per la determinazione, pratica o difesa di 
diritti legali in quei paesi in cui svolgiamo attività di business.  
