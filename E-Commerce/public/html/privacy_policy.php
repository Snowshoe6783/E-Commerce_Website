Protezione dei dati personali dei minori  
La protezione dei dati personali dei minori è di estrema importanza. Legea è consapevole della 
propria obbligazione di proteggere la privacy dei minori, e si rifiuta di rilevare dati di minori di 16 
anni. Invitiamo i genitori e altri tutori a navigare sull'Internet ed inviare e-mail insieme ai loro figli 
e a fruire dell'Internet in modo responsabile.  

